	public function load()
	{
		$id_pabrik = $_REQUEST['id_pabrik'];
		$id_station = $_REQUEST['id_station'];
		$tanggal = $_REQUEST['y']."-".$_REQUEST['m']."-".$_REQUEST['d'];		
		$query = $this->db->query("SELECT unit,acm,keterangan FROM m_acm where id_pabrik = '$id_pabrik' AND id_station = '$id_station' AND tanggal='$tanggal';");

		$i = 0;
		$d = [];
		foreach ($query->result() as $row)
		{
			// $d[$i][0] = $row->nama; // access attributes
			$d[$i][0] = $row->unit; // or methods defined on the 'User' class
			$d[$i][1] = $row->acm; // or methods defined on the 'User' class
			$d[$i++][2] = $row->keterangan; // or methods defined on the 'User' class
			// $d[$i][2] = $row->jenis_breakdown; // or methods defined on the 'User' class
			// $d[$i++][3] = $row->jenis_problem; // or methods defined on the 'User' class
		}
		echo json_encode($d);
	}

	public function simpan()
	{
		$pabrik = $_REQUEST['pabrik'];
		$station = $_REQUEST['station'];
		$tanggal = $_REQUEST['y']."-".$_REQUEST['m']."-".$_REQUEST['d'];
		$this->db->query("DELETE FROM `m_acm` where id_pabrik = '$pabrik' AND id_station = '$station' AND tanggal = '$tanggal' ");
		$data_json = $_REQUEST['data_json'];
		$data = json_decode($data_json);
		foreach ($data as $key => $value) {
			// $this->db->insert
			$data = array(
				'tanggal' => $tanggal,
				'id_pabrik' => $pabrik,
				'id_station' => $station,
				'unit' => $value[0],
				'acm' => $value[1],
				'keterangan' => $value[2],

				// 'jenis_problem' => $value[2],
				// 'jenis_breakdown' => $value[3],
				// 'date' => 'My date'
			);
			// print_r($data);
			if($value[0]!=""){
				$this->db->insert('m_acm', $data);
			}
		}
	}

	public function load_problem(){
		// SELECT max(tanggal) as tanggal,id_station,unit,acm FROM `m_acm` where acm = 1 GROUP by unit
		$id_pabrik = $_REQUEST['id_pabrik'];

		$query = $this->db->query("SELECT max(tanggal) as tanggal,id_station,unit,acm,keterangan FROM `m_acm` where acm = 0 AND id_pabrik = '$id_pabrik' GROUP by unit");

		$i = 0;
		$d = [];
		foreach ($query->result() as $row)
		{
			$d[$i][0] = $row->tanggal; 
			$d[$i][1] = $row->id_station; 
			$d[$i][2] = $row->unit;
			$d[$i][3] = $row->acm;
			$d[$i++][4] = $row->keterangan;
		}
		echo json_encode($d);
	}

